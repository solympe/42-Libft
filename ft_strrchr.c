char	*ft_strrchr(const char *s, int c)
{
	const	char	*tmp;

	tmp = 0;
	while (*s)
	{
		if (*s == c)
		{
			tmp = &(*s);
		}
		s++;
	}
	return ((char *)tmp);
}
