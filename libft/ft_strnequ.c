#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;
	size_t	saver;

	if (!s1 || !s2)
		return (0);
	i = 0;
	saver = n;
	if ((!*s1 && !*s2) || n == 0)
		return (1);
	while ((*s1 != '\0') && (*s2 != '\0') && n)
	{
		if (*s1 == *s2)
		{
			n--;
			i++;
			if (i == saver || ((*(s1 + 1) == '\0') && (*(s2 + 1) == '\0')))
				return (1);
		}
		s1++;
		s2++;
	}
	return (0);
}
